    //Cuando se preciona el boton de guardar
    private void jBGuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuarActionPerformed

        //Vuelve null la matriz de series
        prods.sSers   = null; 
        
        /*Reinicia el arreglo de Modelos*/
        if(jTab.getRowCount()!=0)
             prods.sSers = new String[jTab.getRowCount()][4];

        //Se almacenan los valores con el posible nuevo comentario
        for(int x = 0; x < jTab.getRowCount(); x++)
        {
            /*Inicia el valor en el arreglo*/
            prods.sSers[x][0] = jTab.getValueAt(x, 1).toString();            
            prods.sSers[x][1] = jTab.getValueAt(x, 2).toString();            
            prods.sSers[x][2] = jTab.getValueAt(x, 3).toString();
            prods.sSers[x][3] = jTab.getValueAt(x, 4).toString();
        }

        /*Llama al recolector de basura y cierra la forma*/
        System.gc();
        this.dispose();

    }//GEN-LAST:event_jBGuarActionPerformed

    
